# Função range com for
# A função range(), assim como todos os indices começa em 0 e termina em -1
for i in range(8):
    print(i)
print('...FIM...')

for i in [0, 1, 2, 4, 5, 6, 7]:
    print(i)
print('...FIM...')

# Definindo intervalos com range()
for i in range(1, 10):
    print(i)
print('...Fim...')

